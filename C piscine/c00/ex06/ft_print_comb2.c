#include <unistd.h>

void	putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb2(void)
{
	int	t[2];

	t[0] = 0;
	while (t[0] <= 98)
	{
		t[1] = t[0] + 1;
		while (t[1] <= 99)
		{
			putchar((t[0] / 10) + 48);
			putchar((t[0] % 10) + 48);
			write(1, " ", 1);
			putchar((t[1] / 10) + 48);
			putchar((t[1] % 10) + 48);
			if (t[0] != 98)
				write(1, ", ", 2);
			t[1]++;
		}
		t[0]++;
	}
}
